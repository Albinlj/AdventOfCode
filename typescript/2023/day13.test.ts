const doStuffWithGridString = (
  input: string,
  i: number
) => {
  const matrix = input
    .split('\n')
    .map((line) => line.split(''))

  const row = check(matrix)
  if (row !== undefined) {
    return (row + 1) * 100
  }

  const col = check(transpose(matrix))
  if (col !== undefined) {
    return col + 1
  }

  throw `None found lol ${i} \n` + input
}

